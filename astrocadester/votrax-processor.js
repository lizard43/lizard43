// Helper to mirror MAME's C++ bitswap() macro using modern JavaScript BigInts
function bitswap(val, ...bits) {
    let res = 0;
    for (let i = 0; i < bits.length; i++) {
        const bitPos = BigInt(bits[i]);
        if ((val & (1n << bitPos)) !== 0n) {
            res |= (1 << i);
        }
    }
    return res;
}

class VotraxProcessor extends AudioWorkletProcessor {
    // ... constructor, etc ...

    phone_commit() {
        this.m_phonetick = 0;
        this.m_ticks = 0;

        // In the real chip/MAME logic, it iterates through the ROM array 
        // to find where the upper 6 bits match the requested phoneme code.
        for (let i = 0; i < 64; i++) {
            const val = VOTRAX_ROM[i];
            const romPhoneIndex = Number((val >> 56n) & 0x3fn);

            if (this.m_phone === romPhoneIndex) {
                // Decode internal parameters using the precise bit layouts from MAME
                this.m_rom_f1 = bitswap(val, 0, 7, 14, 21);
                this.m_rom_va = bitswap(val, 1, 8, 15, 22);
                this.m_rom_f2 = bitswap(val, 2, 9, 16, 23);
                this.m_rom_fc = bitswap(val, 3, 10, 17, 24);
                this.m_rom_f2q = bitswap(val, 4, 11, 18, 25);
                this.m_rom_f3 = bitswap(val, 5, 12, 19, 26);
                this.m_rom_fa = bitswap(val, 6, 13, 20, 27);

                // MAME Note: These two values have inverted bit orders due to a die bug workaround
                this.m_rom_cld = bitswap(val, 34, 32, 30, 28);
                this.m_rom_vd = bitswap(val, 35, 33, 31, 29);

                this.m_rom_closure = bitswap(val, 36);

                // Duration bits are inverted (~val)
                this.m_rom_duration = bitswap(~val, 37, 38, 39, 40, 41, 42, 43);

                // Hardwired phone boundaries: 0x03 (PA0) and 0x3E (PA1) are pause markers
                this.m_rom_pause = (this.m_phone === 0x03) || (this.m_phone === 0x3e);

                // Handle edge case check
                if (this.m_rom_cld === 0) {
                    this.m_cur_closure = this.m_rom_closure;
                }
                return;
            }
        }
    }
}

// votrax-processor.js
class VotraxProcessor extends AudioWorkletProcessor {
    constructor() {
        super();
        this.m_phone = 0x3f; // Default STOP phoneme
        this.m_inflection = 0;

        // Port all state variables from C++ device_start/device_reset here
        this.m_pitch = 0;
        this.m_noise = 0x7fff;
        this.m_update_counter = 0;

        // History buffers for the cascaded filters
        this.m_voice_1 = new Float64Array(4);
        this.m_voice_2 = new Float64Array(4);
        // ... and so on for m_voice_3, m_noise_1-4, m_vn_1-6

        // Handle incoming phonemes from the main thread
        this.port.onmessage = (e) => {
            if (e.data.type === 'WRITE_PHONE') {
                this.m_phone = e.data.phone & 0x3f;
                // Run C++ phone_commit() equivalent here to update internal target attributes
            }
            if (e.data.type === 'SET_INFLECTION') {
                this.m_inflection = e.data.inflection & 3;
            }
        };
    }

    // Helper to shift input history arrays
    shiftHist(val, buffer) {
        buffer[3] = buffer[2];
        buffer[2] = buffer[1];
        buffer[1] = buffer[0];
        buffer[0] = val;
    }

    // Ported from C++ apply_filter()
    applyFilter(x, y, a, b) {
        return (x[0] * a[0] + x[1] * a[1] + x[2] * a[2] + x[3] * a[3] - y[1] * b[1] - y[2] * b[2] - y[3] * b[3]) / b[0];
    }

    process(inputs, outputs, parameters) {
        const output = outputs[0];
        const channel = output[0]; // Mono processing

        for (let i = 0; i < channel.length; i++) {
            // 1. Run chip timing logic at chip sample clock frequency
            this.chip_update();

            // 2. Perform the cascading analog path calculations
            let sample = this.analog_calc();

            // 3. Write to Web Audio output buffer
            channel[i] = sample;
        }
        return true;
    }
}

registerProcessor('votrax-processor', VotraxProcessor);